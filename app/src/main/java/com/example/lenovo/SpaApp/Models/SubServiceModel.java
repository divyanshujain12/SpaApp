public class SubServiceModel {
    String subServiceName;
    String subServicePrice;
    String subServiceImageUrl;

    public String getSubServiceName() {
        return subServiceName;
    }

    public void setSubServiceName(String subServiceName) {
        this.subServiceName = subServiceName;
    }

    public String getSubServicePrice() {
        return subServicePrice;
    }

    public void setSubServicePrice(String subServicePrice) {
        this.subServicePrice = subServicePrice;
    }

    public String getSubServiceImageUrl() {
        return subServiceImageUrl;
    }

    public void setSubServiceImageUrl(String subServiceImageUrl) {
        this.subServiceImageUrl = subServiceImageUrl;
    }
}
